"""---
# ⚙️ Section 4 — Hyperparameter Tuning for the Model

A **hyperparameter** is a setting you choose *before* training the model (not learned during training). Tuning them well can significantly improve model performance.

We use **RandomizedSearchCV** to try many random combinations of hyperparameters and find the best configuration for a **Random Forest Regressor**.

### Key Hyperparameters Being Tuned:
| Hyperparameter | Meaning |
|----------------|---------|
| `n_estimators` | Number of trees in the forest — more trees = more stable predictions |
| `max_depth` | Maximum depth of each tree — controls overfitting |
| `min_samples_split` | Minimum samples required to split a node — prevents overly complex trees |
"""

# ============================================================
# Prepare features for modelling (shared preprocessing)
# ============================================================
from sklearn.preprocessing import OneHotEncoder, StandardScaler
from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.ensemble import RandomForestRegressor
from sklearn.linear_model import LinearRegression
from sklearn.tree import DecisionTreeRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score
from sklearn.model_selection import RandomizedSearchCV, train_test_split

# Drop the Size_Bin helper column — it's just a binned version of Size
if 'Size_Bin' in df.columns:
    df.drop(columns=['Size_Bin'], inplace=True)

# Apply log transform to Rent — makes the skewed distribution more normal
# and significantly helps all regression models learn better
# We predict log(Rent) and reverse it back to ₹ after prediction
df['Log_Rent'] = np.log1p(df['Rent'])

# Define features (X) and target (y = log-transformed rent)
TARGET = 'Log_Rent'
drop_cols = ['Rent', 'Log_Rent']
X = df.drop(columns=drop_cols)
y = df[TARGET]
y_actual = df['Rent']   # keep original ₹ values for final metric reporting

# Identify categorical and numeric columns
cat_features = X.select_dtypes(include='object').columns.tolist()
num_features = X.select_dtypes(include='number').columns.tolist()

print(f'Numeric features ({len(num_features)}):     {num_features}')
print(f'Categorical features ({len(cat_features)}): {cat_features}')
print(f'Target: {TARGET} (log-transformed Rent)')

# Shuffled 80/20 train-test split — ensures balanced city and price distribution
X_train, X_test, y_train, y_test, y_train_act, y_test_act = train_test_split(
    X, y, y_actual, test_size=0.2, random_state=42, shuffle=True
)

print(f'\nTraining set size: {X_train.shape[0]} rows')
print(f'Testing set size:  {X_test.shape[0]} rows')

# ============================================================
# Build the preprocessing pipeline
# ============================================================
# StandardScaler normalises numeric columns to mean=0, std=1
# so no single large-valued column (like Size) dominates the model
# One-Hot Encoding converts categorical text into 0/1 numeric columns

preprocessor = ColumnTransformer(transformers=[
    ('num', StandardScaler(), num_features),
    ('cat', OneHotEncoder(handle_unknown='ignore', sparse_output=False), cat_features)
])

print('✅ Preprocessor created!')

# ============================================================
# Hyperparameter Tuning with RandomizedSearchCV
# ============================================================

# Define the hyperparameter search space
param_dist = {
    'model__n_estimators':      [50, 100, 150, 200],     # Number of trees
    'model__max_depth':         [None, 5, 10, 20, 30],   # Tree depth
    'model__min_samples_split': [2, 5, 10],              # Min samples to split a node
}

# Build a pipeline: preprocessing + model
rf_pipeline = Pipeline(steps=[
    ('preprocessor', preprocessor),
    ('model', RandomForestRegressor(random_state=42))
])

# RandomizedSearchCV tries random combinations (faster than GridSearch)
random_search = RandomizedSearchCV(
    estimator=rf_pipeline,
    param_distributions=param_dist,
    n_iter=15,
    cv=3,
    scoring='neg_mean_absolute_error',
    random_state=42,
    n_jobs=-1
)

print('🔍 Running hyperparameter tuning (this may take a minute)...')
random_search.fit(X_train, y_train)

best_params = random_search.best_params_
print(f'\n✅ Best Hyperparameters Found:')
for k, v in best_params.items():
    print(f'   {k}: {v}')
print(f'\n   Best CV MAE (log scale): {-random_search.best_score_:.4f}')

# ============================================================
# Visualize Tuning Results — Top Configurations
# ============================================================
results_df = pd.DataFrame(random_search.cv_results_)
results_df['mean_test_MAE'] = -results_df['mean_test_score']
results_df = results_df.sort_values('mean_test_MAE').head(10)

plt.figure(figsize=(10, 5))
plt.barh(range(len(results_df)), results_df['mean_test_MAE'],
         color=sns.color_palette('Blues_d', len(results_df)))
plt.yticks(range(len(results_df)),
           [f"n_est={r['param_model__n_estimators']}, depth={r['param_model__max_depth']}"
            for _, r in results_df.iterrows()], fontsize=9)
plt.xlabel('Mean Absolute Error (log scale) — Lower is Better', fontsize=11)
plt.title('Top 10 Hyperparameter Combinations by CV MAE', fontsize=13, fontweight='bold')
