# The 'Floor' column contains text like "3 out of 5" or "Ground out of 2"
# We extract two useful numeric features:
#   - 'Floor_Number'  → which floor the property is on (0 = Ground)
#   - 'Total_Floors'  → total floors in the building

def extract_floor(floor_str):
    """Extract floor number and total floors from text like '3 out of 5'."""
    try:
        parts = str(floor_str).lower().split(' out of ')
        floor_num_str = parts[0].strip()
        if floor_num_str == 'ground':
            floor_num = 0
        elif 'upper basement' in floor_num_str:
            floor_num = -1
        elif 'lower basement' in floor_num_str:
            floor_num = -2
        else:
            floor_num = int(floor_num_str)
        total_floors = int(parts[1].strip()) if len(parts) > 1 else np.nan
        return floor_num, total_floors
    except:
        return np.nan, np.nan

